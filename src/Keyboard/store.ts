export const keyCapsData = switchData.map((row, i) =>
	row.map((props, j) => {
		if (i === 0 && j === row.length - 1) {
			return null;
		}
		return {
			position: [props.position[0], -0.27, props.position[2]],
			instanceName: `Row${i + 1}_${getInstanceCodeName(i + 1, j)}`,
			type: 'Primary',
		};
	})
);

function getInstanceCodeName(row: number, index: number) {
	switch (row) {
		case 1:
			return '001';
		case 2:
			return index === 13 ? '002' : '001';
		case 3:
			if (index === 0 || index === 13) {
				return '001';
			} else {
				return '002';
			}
		case 4:
			if (index === 0) {
				return '001';
			} else if (index === 12) {
				return '003';
			} else {
				return '002';
			}
		case 5:
			if (index === 0) {
				return '001';
			} else if (index === 11) {
				return '003';
			} else {
				return '002';
			}
		case 6:
			if (index < 3) {
				return '001';
			} else if (index === 3) {
				return '002';
			} else {
				return '003';
			}
		case 6:
			return '001';

		default:
			return '001';
	}
}
